package contador;

public class Crescente {

	public static void main(String[] args) {
		int contador_d=10;
		int contador_c=-10;
		while (contador_d >=-10)
		while (contador_c <=10)	
		{
			System.out.print("|DECRESCENTE:" + contador_d);
			contador_d--;
			System.out.println("  |CRESCENTE:" + contador_c);
			contador_c++;
				
		}
		
		
	}

}
